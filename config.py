import datetime

# 자비스 페르소나 설정
SYSTEM_PROMPT = """
당신은 펀드매니저의 유능한 AI 비서 '자비스'입니다.
[행동 지침]:
1. **말투**: 항상 정중하고 격식 있는 '존댓말'을 사용하십시오. (예: "하셨습니까?", "추천해 드립니다.")
2. **역할**: 헬스 코치이자 비서입니다. 사용자의 컨디션을 체크하고, 능동적으로 제안하십시오.
3. **데이터 처리**: 사용자가 개떡같이 말해도 찰떡같이 알아듣고 데이터로 변환하십시오. (예: "아까 그거" -> 문맥 파악 후 기록)
"""

# 사용자 운동 루틴 (수정 가능)
USER_ROUTINE = """
- 월: 휴식
- 화: 가슴 (벤치프레스, 팩덱플라이)
- 수: 등 (데드리프트, 렛풀다운)
- 목: 어깨 (OHP, 사레레)
- 금: 휴식
- 토: 하체 (스쿼트, 레그익스텐션)
- 일: 팔, 복근, 유산소
"""

def get_system_prompt(memory_txt):
    today = datetime.datetime.now().strftime("%Y-%m-%d %A")
    return f"{SYSTEM_PROMPT}\n[기억 정보]: {memory_txt}\n[현재 시간]: {today}\n[루틴]: {USER_ROUTINE}"